void main() {
    int a = 42;
    int b = 8;
    System.out.println("Calculator Test:");
    System.out.println(a + " + " + b + " = " + (a + b));
    System.out.println(a + " - " + b + " = " + (a - b));
    System.out.println(a + " × " + b + " = " + (a * b));
    System.out.println(a + " ÷ " + b + " = " + (a / b));
    System.out.println("✅ Math operations working!");
}